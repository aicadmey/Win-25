#  10 examples for else

# Example 1
if 3 > 5:
    print("3 is greater than 5")
else:
    print("3 is not greater than 5")

# Example 2
x = 10
if x < 5:
    print("x is less than 5")
else:
    print("x is 5 or greater")

# Example 3
fruit = input("Enter Fruit: ")
if fruit == "apple":
    print("It's an apple")
else:
    print("It's not an apple")

# Example 4
temperature = 15
if temperature > 20:
    print("It's warm")
else:
    print("It's cold")

# Example 5
status = True
if status:
    print("Status is True")
else:
    print("Status is False")

# Example 6
marks = int(input("Enter you marks: "))
if marks >= 50:
    print("You passed")
else:
    print("You failed")

# Example 7
password = (input("Enter your password: "))
if password == "admin777":
    print("Access granted")
else:
    print("Access denied")

# Example 8
light = "red"
if light == "green":
    print("Go")
else:
    print("Stop")

# Example 9
time = 22
if time < 20:
    print("Good day")
else:
    print("Good night")

